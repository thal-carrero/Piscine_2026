int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*str)
	{
		str++;
		i++;
	}
	return (i);
}

int	ft_validate_base(char *base)
{
	int	i;
	int	j;

	i = 0;
	while (base[i])
	{
		if (base[i] == '+' || base[i] == '-' || base[i] == ' '
			|| (base[i] >= 9 && base[i] <= 13))
			return (0);
		j = i + 1;
		while (base[j])
		{
			if (base[i] == base[j])
				return (0);
			j++;
		}
		i++;
	}
	if (ft_strlen(base) <= 1)
		return (0);
	return (1);
}

int	ft_get_nbr(char c, char *base)
{
	int	i;

	i = 0;
	while (base[i])
	{
		if (c == base[i])
			return (i);
		i++;
	}
	return (-1);
}

int	ft_atoi_base(char *str, char *base)
{
	int	i;
	int	neg_sign;
	int	nb;

	i = 0;
	neg_sign = 0;
	nb = 0;
	if (ft_validate_base(base) == 0)
		return (0);
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg_sign++;
		i++;
	}
	while (ft_get_nbr(str[i], base) != -1)
	{
		nb = nb * ft_strlen(base) + ft_get_nbr(str[i], base);
		i++;
	}
	if (neg_sign % 2 != 0)
		nb = -nb;
	return (nb);
}
